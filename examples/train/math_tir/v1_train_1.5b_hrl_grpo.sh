    trainer.nnodes=$n_nodes \
    +trainer.remove_previous_ckpt_in_save=True \
    trainer.save_freq=10 \
    trainer.test_freq=10 \
    trainer.total_epochs=10 \
    hrl.max_turns=$hrl_max_turns \
    hrl.max_total_tokens=$max_response_length \
    hrl.stop_tokens=$hrl_stop_tokens \
    hrl.selector.model.path=$selector_model_name \
    hrl.selector.model.trust_remote_code=True \
    hrl.selector.rollout.name=vllm \
    hrl.selector.rollout.tensor_model_parallel_size=1 \
    hrl.selector.rollout.data_parallel_size=1 \
    hrl.selector.rollout.pipeline_model_parallel_size=1 \
    hrl.selector.rollout.max_num_seqs=128 \
    hrl.selector.rollout.temperature=$temperature \
    hrl.selector.rollout.top_p=$top_p \
